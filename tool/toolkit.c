uint16_t Modbus_CRC16(uint8_t *updata,uint32_t len)
{
    uint8_t uchCRCHi = 0;
    uint8_t uchCRCLo = 0;
    uint32_t  uindex = 0;
    uint16_t ret = 0;
    uint16_t u16crc = 0xffff;
    uchCRCHi = (u16crc >> 8)&0xff;
    uchCRCLo = (u16crc)&0xff;
    while(len--)
    {
        uindex=uchCRCHi^*updata++;
        uchCRCHi=uchCRCLo^auchCRCHi[uindex];
        uchCRCLo=auchCRCLo[uindex];
    }
    ret = uchCRCLo|(uchCRCHi << 8);
    return (ret);
}
